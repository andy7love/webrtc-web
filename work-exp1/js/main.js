var localConsoleWindow = document.querySelector('#local-messages');
var remoteConsoleWindow = document.querySelector('#remote-messages');

var isChannelReady = false;
var isInitiator = false;
var isStarted = false;
var readyToSend = false;
var pc;
var turnReady;
var sendChannel;
var receiveChannel;

